//! Brick glyph geometry — the per-half-brick SVG path segments that draw each
//! signal "brick" (clock edge, level, transition, data box, hi-Z, undefined).
//!
//! Faithfully ported from `references/wavedrom/skins/default.js`: each glyph is
//! a 20×20 cell drawn at `x = i * xs`. WaveDrom's skin bakes the data-bus fill
//! color into the glyph name (`vvv-2` … `vvv-9`); we instead resolve the fill
//! from `opts.series_palette` at draw time so the host palette controls colors.
//! The skin's stroke "classes" (s1/s2/s3/…) become a small [`PathKind`] enum.

use std::collections::HashMap;
use std::fmt::Write as _;
use std::sync::OnceLock;

use crate::svgutil::{opacity_attr, rgb};

/// How a brick's path segment is stroked/filled.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum PathKind {
    /// Solid foreground stroke, width 1 (`s1`). The signal line.
    S1,
    /// Thin foreground stroke, width 0.5 (`s2`). Hatching on `x`/`u`/`d` bricks.
    S2,
    /// Dashed foreground stroke (`s3`/`s4`). Weak/pull (`d`/`u`/`z`) hints.
    Dash,
    /// Foreground fill, no stroke (`s6`). Clock-edge arrow markers (P/N).
    FillBlack,
    /// Data-bus box fill — color resolved from the glyph name's `-N` suffix.
    FillData,
}

use PathKind as K;

/// `(path_d, kind)` for one segment of a glyph.
type Seg = (&'static str, PathKind);

/// Static glyph table: name → list of path segments (all in a 20×20 cell).
fn glyphs() -> &'static HashMap<&'static str, &'static [Seg]> {
    static G: OnceLock<HashMap<&'static str, &'static [Seg]>> = OnceLock::new();
    G.get_or_init(|| GLYPH_TABLE.iter().copied().collect())
}

/// Look up a brick glyph's segments by name (e.g. `"pclk"`, `"0m1"`, `"vvv-3"`).
/// Unknown names fall back to the `xxx` (undefined) glyph.
pub fn glyph(name: &str) -> &'static [Seg] {
    let g = glyphs();
    g.get(name).copied().unwrap_or_else(|| g.get("xxx").copied().unwrap_or(&[]))
}

/// The trailing `-N` color index of a data brick name (`vvv-3` → `Some(3)`),
/// else `None`. For double-data transitions (`vmv-2-3`) the *last* `-N` is the
/// new (right-hand) segment's color, which is what fills the box.
pub fn data_color_index(name: &str) -> Option<u8> {
    name.rsplit('-').next().and_then(|tok| tok.parse::<u8>().ok()).filter(|n| (2..=9).contains(n))
}

/// Resolve a data color index (2..9) to an RGBA from the palette (index 2 → 0).
pub fn palette_color(idx: u8, palette: &[[u8; 4]]) -> [u8; 4] {
    if palette.is_empty() {
        return [230, 230, 230, 255];
    }
    let i = (idx.saturating_sub(2)) as usize % palette.len();
    palette[i]
}

/// Emit one brick glyph translated to `(ox, oy)` into `svg`.
///
/// `fg` is the foreground (stroke) color; `data_fill` is the resolved fill for
/// this glyph's data box (if any). The brick is drawn at scale 1 in X (callers
/// stretch lanes by repeating half-bricks, matching WaveDrom) but `xscale`
/// lets `hscale`/`period`-wide bricks stretch a *single* glyph horizontally —
/// here we keep glyphs unit-width and rely on repetition, so `xscale == 1`.
pub fn emit_brick(
    svg: &mut String,
    name: &str,
    ox: f32,
    oy: f32,
    fg: [u8; 4],
    data_fill: Option<[u8; 4]>,
) {
    let segs = glyph(name);
    if segs.is_empty() {
        return;
    }
    let _ = write!(svg, "<g transform=\"translate({ox:.2},{oy:.2})\">");
    for (d, kind) in segs {
        match kind {
            K::S1 => {
                let _ = write!(
                    svg,
                    "<path d=\"{d}\" fill=\"none\" stroke=\"{c}\"{o} stroke-width=\"1\" \
                     stroke-linecap=\"round\"/>",
                    c = rgb(fg),
                    o = opacity_attr("stroke-opacity", fg),
                );
            }
            K::S2 => {
                let _ = write!(
                    svg,
                    "<path d=\"{d}\" fill=\"none\" stroke=\"{c}\"{o} stroke-width=\"0.5\" \
                     stroke-linecap=\"round\"/>",
                    c = rgb(fg),
                    o = opacity_attr("stroke-opacity", fg),
                );
            }
            K::Dash => {
                let _ = write!(
                    svg,
                    "<path d=\"{d}\" fill=\"none\" stroke=\"{c}\"{o} stroke-width=\"1\" \
                     stroke-dasharray=\"1,3\" stroke-linecap=\"round\"/>",
                    c = rgb(fg),
                    o = opacity_attr("stroke-opacity", fg),
                );
            }
            K::FillBlack => {
                let _ = write!(
                    svg,
                    "<path d=\"{d}\" fill=\"{c}\"{o} stroke=\"none\"/>",
                    c = rgb(fg),
                    o = opacity_attr("fill-opacity", fg),
                );
            }
            K::FillData => {
                let fill = data_fill.unwrap_or([230, 230, 230, 255]);
                let _ = write!(
                    svg,
                    "<path d=\"{d}\" fill=\"{c}\"{o} stroke=\"none\"/>",
                    c = rgb(fill),
                    o = opacity_attr("fill-opacity", fill),
                );
            }
        }
    }
    svg.push_str("</g>");
}

/// The gap glyph (`#gap`) — a white-masked zig drawn over a lane break (`|`).
/// Ported verbatim from the skin; drawn translated to the break's `(x, y)`.
pub fn emit_gap(svg: &mut String, ox: f32, oy: f32, fg: [u8; 4]) {
    let _ = write!(svg, "<g transform=\"translate({ox:.2},{oy:.2})\">");
    // s5: white backdrop wedge.
    let _ = write!(
        svg,
        "<path d=\"m7,-2 -4,0 c -5,0 -5,24 -10,24 l 4,0 C 2,22 2,-2 7,-2 z\" \
         fill=\"#ffffff\" stroke=\"none\"/>"
    );
    // s1: the two curved break edges.
    for d in ["M-7,22 C -2,22 -2,-2 3,-2", "M-3,22 C 2,22 2,-2 7,-2"] {
        let _ = write!(
            svg,
            "<path d=\"{d}\" fill=\"none\" stroke=\"{c}\"{o} stroke-width=\"1\" \
             stroke-linecap=\"round\"/>",
            c = rgb(fg),
            o = opacity_attr("stroke-opacity", fg),
        );
    }
    svg.push_str("</g>");
}

